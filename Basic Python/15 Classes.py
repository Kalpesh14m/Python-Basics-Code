# Classes quickly work their way into "intermediate" programming, so hopefully I can just help you understand how they work and how to follow code that uses them.
# Classes are the backbone to Object Oriented Programming, or OOP.

class calculator:

    def addition(x,y):
        added = x + y
        print(added)

    def subtraction(x,y):
        sub = x - y
        print(sub)

    def multiplication(x,y):
        mult = x * y
        print(mult)

    def division(x,y):
        div = x / y
        print(div)


calculator.addition(10,20)

calculator.subtraction(20,10)

calculator.multiplication(10,3)

calculator.division(10,2)
